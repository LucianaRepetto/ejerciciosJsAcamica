var tecla = "izquierda";

switch(tecla){
    case "izquierda":
        alert("Tecla Izquierda")
        break;
    case "derecha":
        alert("Tecla Derecha")
        break;
    case "arriba":
        alert("Tecla Arriba")
        break;
    case "abajo":
        alert("Tecla Abajo")
        break;
    default:
        alert("Tecla Incorrecta")
};